def get_next_advice(user_id, category):
    key = f"{user_id}_{category}"
    if key not in _advice_idx:
        _advice_idx[key] = 0
    items = ADVICE.get(category, ADVICE["other"])
    idx = _advice_idx[key]
    _advice_idx[key] = (idx + 1) % len(items)
    return items[idx]

def choose_phrase(data, kind, name="Зайка"):
    mode = data.get("mode", "bold")
    variants = PHRASES.get(kind, {}).get(mode, [])
    if not variants:
        return ""
    recent = data.get("recent_phrases", [])
    candidates = [x for x in variants if x not in recent[-7:]] or variants
    picked = random.choice(candidates)
    data["recent_phrases"] = (recent + [picked])[-30:]
    return picked.format(name=name)

def choose_expense_mix(data, category=None, name="Зайка"):
    roll = random.random()
    if roll < 0.50:
        kind = "expense_reaction"
    elif roll < 0.70:
        kind = "roast"
    elif roll < 0.85:
        kind = "insight"
    elif roll < 0.95:
        kind = "support"
    else:
        kind = "roast"
    phrase = choose_phrase(data, kind, name=name)
    if category == "shopping" and random.random() < 0.45:
        phrase = random.choice(market_phrases).format(name=name)
    elif category == "dates" and random.random() < 0.45:
        phrase = random.choice(men_phrases).format(name=name)
    elif category in CATEGORY_CONTEXT and random.random() < 0.35:
        phrase += f" Контекст: {random.choice(CATEGORY_CONTEXT[category])}."
    return phrase
